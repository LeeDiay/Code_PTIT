package CodePTIT;

import java.util.Scanner;

/**
 *
 * @author DELL
 */
public class J01026 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        long t = sc.nextLong();
        while (t-- > 0) {
            int n = sc.nextInt();
            int uoc = (int)Math.sqrt(n);
            if (n == uoc * uoc) {
                System.out.println("YES");
            }
            else System.out.println("NO");
        }
    }
}
